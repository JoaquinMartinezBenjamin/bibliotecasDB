    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed

        int  no_usuario=Integer.parseInt(txt_nousuario.getText());
        int no_tarjeta= Integer.parseInt(txt_notarjeta.getText());
        String departamento= txt_departamento.getText();
        String titulo= txt_titulo.getText();
        p.nuevoProfesor(no_usuario,no_tarjeta,departamento,titulo);
        updateTabla();
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if (fila > -1){
            String codigo = String.valueOf(tabla.getValueAt(fila, 0));
            System.out.println("Eliminando registro con pk "+codigo);
            p.deleteProfesor(codigo);
            updateTabla();
            fila=-1;
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        if (fila > -1){
        int no_usuario=Integer.parseInt(txt_nousuario.getText());
        int no_tarjeta= Integer.parseInt(txt_notarjeta.getText());
        String departamento= txt_departamento.getText();
        String titulo= txt_titulo.getText();

            p.updateProfesor(no_usuario,no_tarjeta,departamento,titulo);

            updateTabla();
        }
    }//GEN-LAST:event_actualizarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        fila = tabla.rowAtPoint(evt.getPoint());
        if (fila > -1){
            txt_nousuario.setText(String.valueOf(tabla.getValueAt(fila, 0)));
            txt_notarjeta.setText(String.valueOf(tabla.getValueAt(fila, 1)));
            txt_departamento.setText(String.valueOf(tabla.getValueAt(fila, 2)));
            txt_titulo.setText(String.valueOf(tabla.getValueAt(fila, 3)));

        }
    }//GEN-LAST:event_tablaMouseClicked

