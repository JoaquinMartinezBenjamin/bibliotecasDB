    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        fila = tabla.rowAtPoint(evt.getPoint());
        if (fila > -1){
            txt_folio.setText(String.valueOf(tabla.getValueAt(fila, 0)));
           
            txt_idejemplar.setText(String.valueOf(tabla.getValueAt(fila, 2)));
            txt_observaciones.setText(String.valueOf(tabla.getValueAt(fila, 3)));

        }
    }//GEN-LAST:event_tablaMouseClicked

