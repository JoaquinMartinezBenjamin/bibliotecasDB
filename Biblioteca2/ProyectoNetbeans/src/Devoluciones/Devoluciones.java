    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed

        int folio= Integer.parseInt(txt_folio.getText());
        int id_ejemplar=Integer.parseInt(txt_idejemplar.getText());
        String observaciones=txt_observaciones.getText();
        p.nuevaDevolucion(folio,id_ejemplar,observaciones);
        updateTabla();
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if (fila > -1){
            String codigo = String.valueOf(tabla.getValueAt(fila, 0));
            System.out.println("Eliminando registro con pk "+codigo);
            p.deleteDevolucion(codigo);
            updateTabla();
            fila=-1;
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        if (fila > -1){
            int folio= Integer.parseInt(txt_folio.getText());
        int id_ejemplar=Integer.parseInt(txt_idejemplar.getText());
        String observaciones=txt_observaciones.getText();

            p.updateDevolucion(folio,id_ejemplar,observaciones);

            updateTabla();
        }
    }//GEN-LAST:event_actualizarActionPerformed

