    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed

        int  isbn=Integer.parseInt(txt_isbn.getText());
        String titulo= txt_titulo.getText();
        String editorial= txt_editorial.getText();
        String pais= txt_pais.getText();
        int  anio=Integer.parseInt(txt_anio.getText());

        p.nuevoLibro(isbn,titulo,editorial,pais,anio);
        updateTabla();
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if (fila > -1){
            String codigo = String.valueOf(tabla.getValueAt(fila, 0));
            System.out.println("Eliminando registro con pk "+codigo);
            p.deleteLibro(codigo);
            updateTabla();
            fila=-1;
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        if (fila > -1){
         int  isbn=Integer.parseInt(txt_isbn.getText());
        String titulo= txt_titulo.getText();
        String editorial= txt_editorial.getText();
        String pais= txt_pais.getText();
        int  anio=Integer.parseInt(txt_anio.getText());
            p.updateLibro(isbn,titulo,editorial,pais, anio);

            updateTabla();
        }
    }//GEN-LAST:event_actualizarActionPerformed

