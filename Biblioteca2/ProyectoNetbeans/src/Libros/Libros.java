    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        fila = tabla.rowAtPoint(evt.getPoint());
        if (fila > -1){
            txt_isbn.setText(String.valueOf(tabla.getValueAt(fila, 0)));
            txt_titulo.setText(String.valueOf(tabla.getValueAt(fila, 1)));
            txt_editorial.setText(String.valueOf(tabla.getValueAt(fila, 2)));
            txt_pais.setText(String.valueOf(tabla.getValueAt(fila, 3)));
            txt_anio.setText(String.valueOf(tabla.getValueAt(fila, 4)));
        }
    }//GEN-LAST:event_tablaMouseClicked

